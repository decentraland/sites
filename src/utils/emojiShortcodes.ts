// Slack shortcode rules: ASCII letters, digits, underscore, hyphen, plus. 1-32 chars between colons.
const SHORTCODE_REGEX = /:([a-z0-9_+-]{1,32}):/gi

const replaceEmojiShortcodes = (text: string): string =>
  text.replace(SHORTCODE_REGEX, (match, name: string) => EMOJI_SHORTCODES.get(name.toLowerCase()) ?? match)

export { EMOJI_SHORTCODES, replaceEmojiShortcodes }
